import math

def add(self, a, b):
    print(f'The Addition of {a} and {b} is {a+b}')
def sub(self, a, b):
    print(f'The subtraction of {a} and {b} is {a-b}')
def mul(self, a, b):
    print(f'The multiplication of {a} and {b} is {a*b}')
def div(self, a, b):
    print(f'The division of {a} and {b} is {a/b}')
def sq(self, a):
    print(f'The square of {a} is {a**2}')
def cube(self, a):
    print(f'The cube of {a} is {a**3}')
def sqrt(self, a):
    print(f'The square root of {a} is {int(math.sqrt(a))}')


